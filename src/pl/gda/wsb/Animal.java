package pl.gda.wsb;

public class Animal {

    public String name;
    public int age;
    public int height;
    public boolean haveTail;

    public Animal(String name,
                  int age,
                  int height,
                  boolean haveTail) {
        this.name = name;
        this.age = age;
        this.height = height;
        this.haveTail = haveTail;
    }
}
